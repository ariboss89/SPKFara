    private void ShowId(){
        con = new Koneksi();
        try{
            st = con.connect().createStatement();
            res = st.executeQuery("SELECT *FROM tb_penyebab");
            while(res.next()){
                cbKode.addItem(res.getString("Id"));
            }
        }catch(SQLException ex){
            
        }
    }
